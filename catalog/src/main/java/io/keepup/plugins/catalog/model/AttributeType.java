package io.keepup.plugins.catalog.model;

/**
 * Set of attribute types for UI editors
 */
public enum AttributeType {
    /**
     * Text attribute
     */
    TEXT,
    /**
     * Image attribute
     */
    IMAGE,
    /**
     * File attribute
     */
    FILE,
    /**
     * HTML attribute
     */
    HTML,
    /**
     * Boolean attribute
     */
    BOOLEAN,
    /**
     * Enum attribute
     */
    ENUM,
    /**
     * Array attribute
     */
    ARRAY
}
